#include "../minishell.h"

void	*ft_free_int(int **fd)
{
	int	i;

	i = 0;
	if (!fd)
		return (NULL);
	while (fd[i])
	{
		free(fd[i]);
		i++;
	}
	free(fd);
	return (NULL);
}
