#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	k;

	i = 0;
	if (s)
	{
		k = ft_strlen(s);
		while (i < k)
		{
			write(fd, &s[i], 1);
			i++;
		}
	}
}
